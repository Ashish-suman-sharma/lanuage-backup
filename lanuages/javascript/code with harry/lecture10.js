// while loops

let n = 5;
let i = 0;
while (i < n) {
    console.log(i);
    i++;
}


// do while loop

let a = 5;
let j = 0;
do {
    console.log(j);
    j++;
} while (j < a);

//difrrence between while and do while loop is that in while loop first condition is checked and then code is executed but in do while loop first code is executed and then condition is checked. so in do while loop code is executed atleast once.